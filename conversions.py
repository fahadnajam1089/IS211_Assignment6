def convertCelsiusToKelvin(temp):
    return temp + 273.15

def convertCelsiusToFahrenheit(temp):
    return (temp * 9/5) + 32

def convertFarhenheitToCelsius(temp):
    return (temp - 32) * 5 / 9

def convertFarhenheitToKelvin(temp):
    return (temp - 32) * 5/9 + 273.15

